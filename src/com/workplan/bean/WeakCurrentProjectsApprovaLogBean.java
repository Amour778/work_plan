package com.workplan.bean;

public class WeakCurrentProjectsApprovaLogBean {

	private String id;

	// 事项唯一ID
	private String item_id;

	// 审批事项:立项，奖金分配等
	private String item;
	
	// 审批时间
	private String approval_time;
	
	// 同意，驳回
	private String approval_sta;
	
	// 审批人
	private String approval_user;
	
	// 审批备注
	private String remark;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getItem_id() {
		return item_id;
	}

	public void setItem_id(String itemId) {
		item_id = itemId;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public String getApproval_time() {
		return approval_time;
	}

	public void setApproval_time(String approvalTime) {
		approval_time = approvalTime;
	}

	public String getApproval_sta() {
		return approval_sta;
	}

	public void setApproval_sta(String approvalSta) {
		approval_sta = approvalSta;
	}

	public String getApproval_user() {
		return approval_user;
	}

	public void setApproval_user(String approvalUser) {
		approval_user = approvalUser;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	
}
